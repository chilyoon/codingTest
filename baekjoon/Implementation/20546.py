import sys
input = sys.stdin.readline

money = int(input())
duck = list(map(int, input().split()))

jh, sm = 0, 0
jhM, smM = money, money

for i in range(13):
    ju = jhM // duck[i]
    if ju > 0:
        jh += ju
        jhM -= (duck[i]*ju)

jhM += (duck[-1] * jh)

for i in range(11):
    # 3ì¼ ì—°ì† í•˜ë½ ì „ëŸ‰ ë§¤ìˆ˜
    if duck[i] > duck[i+1] > duck[i+2] > duck[i+3]:
        sm += (smM // duck[i+3])
        smM -= ((smM // duck[i+3]) * duck[i+3])
    # 3ì¼ ì—°ì† ìƒìŠ¹ ì „ëŸ‰ ë§¤ë„
    if sm > 0 and duck[i] < duck[i+1] < duck[i+2] < duck[i+3]:
        smM += (sm*duck[i+3])
        sm = 0

smM += (duck[-1] * sm)

# print(jhM, smM)
if jhM > smM:
    print('BNP')
elif jhM < smM:
    print('TIMING')
else:
    print('SAMESAME')